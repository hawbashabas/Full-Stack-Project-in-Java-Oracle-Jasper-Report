    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
       
    MyConnection obj=new MyConnection();
    conn= obj.connectionDB(); 
    
    String strEID=txtEID.getText();
    int eid=Integer.parseInt(strEID);
    
    String fullname=txtFullname.getText();
    String gender=cmbGender.getSelectedItem().toString();
  
    String strSalary=txtSalary.getText();
    double salary=Double.parseDouble(strSalary);
    
    Date startWork=new Date(txtStartWork.getDate().getTime());
    
    String strDID=cmbDID.getSelectedItem().toString();
    int did=Integer.parseInt(strDID);
    
    
    try{
      String sql ="insert into Employee (eid,fullname,gender,salary,start_work,did)"
              + "values (?,?,?,?,?,?)";
      
      ps=conn.prepareStatement(sql);
      ps.setInt(1,eid);
      ps.setString(2,fullname);
      ps.setString(3,gender);
      ps.setDouble(4, salary);
      ps.setDate(5, startWork);
      ps.setInt(6, did);
      
      ps.executeUpdate();
      JOptionPane.showMessageDialog(null, "Insert successfully!");
    
      getEmployee();
      
      txtEID.setText("");
      txtFullname.setText("");
      cmbGender.setSelectedIndex(-1);
      txtSalary.setText("");
      txtStartWork.setDate(null);
      cmbDID.setSelectedIndex(-1);
      
      
      conn.close();
      
     
    }catch(SQLException e){
        System.out.println("Error in SQL Insert command from employee"+e);
    }

        
    }//GEN-LAST:event_btnInsertActionPerformed

