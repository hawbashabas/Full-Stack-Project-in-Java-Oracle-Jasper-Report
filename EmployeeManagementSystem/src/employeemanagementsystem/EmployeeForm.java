    private void tableEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEmployeeMouseClicked
     
     int rows=-tableEmployee.getRowCount();
        if(rows!=0){
            int selected_row=tableEmployee.getSelectedRow();
            String eid=tableEmployee.getValueAt(selected_row, 0).toString();
            String fname=tableEmployee.getValueAt(selected_row, 1).toString();
            String gender=tableEmployee.getValueAt(selected_row, 2).toString();
            String salary=tableEmployee.getValueAt(selected_row, 3).toString();
            Date startWork=(Date) tableEmployee.getValueAt(selected_row, 4);
            String did=tableEmployee.getValueAt(selected_row, 5).toString();
            
            
            txtEID.setText(eid);
            txtFullname.setText(fname);
            cmbGender.setSelectedItem(gender);
            txtSalary.setText(salary);
            txtStartWork.setDate(startWork);
            cmbDID.setSelectedItem(did);
            
            
            
        }
        
    }//GEN-LAST:event_tableEmployeeMouseClicked

